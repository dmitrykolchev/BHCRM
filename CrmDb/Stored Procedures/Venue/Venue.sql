CREATE VIEW [dbo].[Venue]
as
	select
		[Account].[Id],
		[Account].[State],
		[Account].[FileAs],
		[Account].[FullName],
		[Account].[MemberOfAccountId],
		[Account].[TradeMarkId],
		[Account].[AccountTypeId],
		[Account].[AccountGroupId],
		[Account].[IndustryId],
		[Account].[RegionId],
		[Account].[Employees],
		[Account].[AnnualRevenue],
		[Account].[ManagingOrganizationId],
		[Account].[AssignedToEmployeeId],
		[Account].[AssistantEmployeeId],
		[Account].[ExecutiveId],
		[Account].[AccountantId],
		[Account].[Phone],
		[Account].[OtherPhone],
		[Account].[Fax],
		[Account].[Email],
		[Account].[OtherEmail],
		[Account].[WebSite],
		[Account].[BillingAddressStreet],
		[Account].[BillingAddressCity],
		[Account].[BillingAddressRegion],
		[Account].[BillingAddressPostalCode],
		[Account].[BillingAddressCountry],
		[Account].[ShippingAddressStreet],
		[Account].[ShippingAddressCity],
		[Account].[ShippingAddressRegion],
		[Account].[ShippingAddressPostalCode],
		[Account].[ShippingAddressCountry],
		[Account].[Comments],
		[VenueData].[VenuePlaceId],
		[VenueData].[PrimaryVenueTypeId],
		[VenueData].[SecondaryVenueTypeId],
		[VenueData].[Summer],
		[VenueData].[Winter],
		[VenueData].[MaximumNumberOfGuestsForBanquet],
		[VenueData].[MaximumNumberOfGuestsForReception],
		[VenueData].[CateringTypeId],
		[Account].[Created],
		[Account].[CreatedBy],
		[Account].[Modified],
		[Account].[ModifiedBy],
		[Account].[RowVersion]
	from
		[dbo].[Account] left outer join [dbo].[VenueData]
			on ([Account].[Id] = [VenueData].[Id])
	where
		([Account].[AccountTypeId] & 0x10) = 0x10;